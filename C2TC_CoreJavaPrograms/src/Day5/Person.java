package Day5;

public class Person {

		private int pid;
		private String name;
		private String city;
		public int getpid() {
			return pid;
		}
		public void setPid(int pid) {
			this.pid = pid;
		}
		public String getName() {
			return name;
		}
		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}
		public void setName(String name) {
			this.name = name;
		}
		
}
