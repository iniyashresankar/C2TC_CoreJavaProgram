package DayOne;

public class DatatypesDemo {

	public static void main(String[] args) {
		
		int value1 = 9 / 4; 
		float value2 = 100 / 88;
		double value3 = 45d / 8d;
		float value4 = 4 / 2.5f;
		
		System.out.println("\"int\" "+value1 +" \"float\" "+value2 +" \"double\" "+value3 +" \"float division\" "+value4);
		
		String var1 = "Welcome for learning java";
		char var2 = 'A';
		System.out.println(var1 +"\n"+ var2);
		
		//expression 
		int side = 4;
		float area = side * side;
		System.out.println("Area of square "+area);
	}

}
